-- Helper function to extract current working directory for Windows
function get_current_working_dir(tab)
	local cwd_uri = tab.active_pane.current_working_dir
	if cwd_uri then
		-- Remove the 'file:///' prefix and decode any percent-encoded characters
		local cwd = cwd_uri:sub(8):gsub("%%(%x%x)", function(hex)
			return string.char(tonumber(hex, 16))
		end)

		-- For Windows, extract the last part of the path after the last backslash
		return cwd:match("([^\\]+)$") or cwd
	end
	return "[No CWD]"
end

-- Helper function to get the active process name in the pane
function get_process(tab)
	local process_name = tab.active_pane.foreground_process_name
	if process_name then
		return process_name:match("([^/\\]+)$") -- Extract the process name from the full path
	end
	return nil
end

wezterm.on("format-tab-title", function(tab, tabs, panes, config, hover, max_width)
	local has_unseen_output = false
	if not tab.is_active then
		for _, pane in ipairs(tab.panes) do
			if pane.has_unseen_output then
				has_unseen_output = true
				break
			end
		end
	end

	-- Get current directory and process
	local cwd = wezterm.format({ { Text = get_current_working_dir(tab) } })
	local process = get_process(tab)

	-- Construct the tab title
	local title = process and string.format(" %s (%s) ", process, cwd) or " [?] "

	-- If the tab has unseen output, change the title color
	if has_unseen_output then
		return {
			{ Foreground = { Color = "#28719c" } },
			{ Text = title },
		}
	end

	return {
		{ Text = title },
	}
end)

-- tmux status
wezterm.on("update-right-status", function(window, _)
	local SOLID_LEFT_ARROW = ""
	local ARROW_FOREGROUND = { Foreground = { Color = "#c6a0f6" } }
	local prefix = ""

	if window:leader_is_active() then
		prefix = " " .. utf8.char(0x1f30a) -- ocean wave
		SOLID_LEFT_ARROW = utf8.char(0xe0b2)
	end

	if window:active_tab():tab_id() ~= 0 then
		ARROW_FOREGROUND = { Foreground = { Color = "#1e2030" } }
	end -- arrow color based on if tab is first pane

	window:set_left_status(wezterm.format({
		{ Background = { Color = "#b7bdf8" } },
		{ Text = prefix },
		ARROW_FOREGROUND,
		{ Text = SOLID_LEFT_ARROW },
	}))
end)

